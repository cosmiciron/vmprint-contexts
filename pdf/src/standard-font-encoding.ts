const reverseMap = (input: Readonly<Record<number, number>>): Readonly<Record<number, number>> => {
    const out: Record<number, number> = {};
    for (const [byteString, codePoint] of Object.entries(input)) {
        const byte = Number(byteString);
        if (out[codePoint] === undefined) {
            out[codePoint] = byte;
        }
    }
    return out;
};

const SYMBOL_UNICODE_TO_BYTE = reverseMap(SYMBOL_CODE_TO_UNICODE);
const DINGBATS_UNICODE_TO_BYTE = reverseMap(DINGBATS_CODE_TO_UNICODE);

const WIN_ANSI_FONTS = new Set<StandardPostscriptFontName>([
    'Helvetica',
    'Helvetica-Bold',
    'Helvetica-Oblique',
    'Helvetica-BoldOblique',
    'Times-Roman',
    'Times-Bold',
    'Times-Italic',
    'Times-BoldItalic',
    'Courier',
    'Courier-Bold',
    'Courier-Oblique',
    'Courier-BoldOblique'
]);

const supportedWinAnsiByte = (codePoint: number): number | undefined => {
    const mapped = WIN_ANSI_UNICODE_TO_BYTE[codePoint];
    if (mapped !== undefined) return mapped;
    if (codePoint >= 0x20 && codePoint <= 0x7e) return codePoint;
    if (codePoint >= 0xa0 && codePoint <= 0xff) return codePoint;
    return undefined;
};

const encodeSupportedCodePoint = (
    postscriptName: StandardPostscriptFontName,
    codePoint: number
): number | undefined => {
    if (WIN_ANSI_FONTS.has(postscriptName)) {
        return supportedWinAnsiByte(codePoint);
    }
    if (postscriptName === 'Symbol') {
        return SYMBOL_UNICODE_TO_BYTE[codePoint];
    }
    if (postscriptName === 'ZapfDingbats') {
        return DINGBATS_UNICODE_TO_BYTE[codePoint];
    }
    return undefined;
};

const getUnsupportedGlyphEncoding = (postscriptName: StandardPostscriptFontName): number => {
    if (postscriptName === 'Symbol' || postscriptName === 'ZapfDingbats') {
        return 0x20;
    }
    return 0x20;
};

export const encodeStandardFontText = (
    metadata: StandardFontMetadata | StandardPostscriptFontName,
    text: string
): StandardEncodedRun => {
    const postscriptName = typeof metadata === 'string' ? metadata : metadata.postscriptName;
    const metrics = getStandardAfmMetrics(postscriptName);
    const glyphs: StandardEncodedGlyph[] = [];
    const positions: StandardEncodedPosition[] = [];

    for (const character of text || '') {
        const codePoint = character.codePointAt(0);
        if (codePoint === undefined) continue;

        const encodedByte = encodeSupportedCodePoint(postscriptName, codePoint);
        const width = metrics.widthsByCode[codePoint];
        const supported = encodedByte !== undefined && Number.isFinite(width);
        const advanceWidth = supported ? width : metrics.defaultWidth;
        const nominalWidth = supported ? width : metrics.defaultWidth;
        const emittedByte = supported ? encodedByte : getUnsupportedGlyphEncoding(postscriptName);

        glyphs.push({
            id: supported ? codePoint + 1 : 0,
            codePoints: [codePoint],
            advanceWidth,
            nominalWidth,
            encodedByte: emittedByte,
            supported
        });
        positions.push({
            xAdvance: advanceWidth,
            xOffset: 0,
            yOffset: 0
        });
    }

    const totalAdvance = positions.reduce((sum, pos) => sum + pos.xAdvance, 0);
    return { glyphs, positions, totalAdvance };
};
